// Vendors @mozilla/readability's Readability.js into a TS module so it can be
// injected into the extraction WebView as a string. Run via:
//   node scripts/vendor-readability.mjs   (or: pnpm vendor:readability)

import { createRequire } from "node:module";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..");

/** Resolve a file inside @mozilla/readability, robust to pnpm symlinks. */
function resolveReadabilityFile(relPath) {
  try {
    const require = createRequire(join(repoRoot, "package.json"));
    return require.resolve(`@mozilla/readability/${relPath}`);
  } catch {
    // Fallback: read through the node_modules symlink directly.
    return join(repoRoot, "node_modules", "@mozilla", "readability", relPath);
  }
}

const sourcePath = resolveReadabilityFile("Readability.js");
const pkgPath = resolveReadabilityFile("package.json");

const source = readFileSync(sourcePath, "utf8");
const { version } = JSON.parse(readFileSync(pkgPath, "utf8"));

const out = `// @generated by scripts/vendor-readability.mjs — do not edit.
// Source: @mozilla/readability ${version} (Readability.js), Apache-2.0.
export const READABILITY_SOURCE: string = ${JSON.stringify(source)};
`;

const outPath = join(repoRoot, "src", "lib", "readabilitySource.ts");
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out);

console.log(
  `Wrote ${outPath} (${source.length} chars, @mozilla/readability ${version})`
);
